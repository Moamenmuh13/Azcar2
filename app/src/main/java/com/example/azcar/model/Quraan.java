package com.example.azcar.model;

public class Quraan    {
    public String  id;
    public String soraName;
    public String soraNum;


    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getSoraName() {
        return soraName;
    }

    public void setSoraName(String soraName) {
        this.soraName = soraName;
    }

    public String getSoraNum() {
        return soraNum;
    }

    public void setSoraNum(String soraNum) {
        this.soraNum = soraNum;
    }
}
